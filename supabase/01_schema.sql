create extension if not exists pgcrypto;

create table if not exists public.perfis (
  id bigint generated by default as identity primary key,
  nome text not null unique,
  cor text default '#d6a84f',
  created_at timestamptz default now()
);

create table if not exists public.livros (
  id uuid primary key default gen_random_uuid(),
  codigo text unique not null,
  titulo text not null,
  autor text,
  tema text,
  subtema text,
  colecao text,
  volume text,
  editora_formato text,
  isbn text,
  paginas integer,
  ano integer,
  localizacao text,
  capa_url text,
  observacoes text,
  tags text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.leituras (
  id uuid primary key default gen_random_uuid(),
  livro_id uuid not null references public.livros(id) on delete cascade,
  perfil_id bigint not null references public.perfis(id) on delete cascade,
  lido boolean not null default false,
  nota numeric(3,1) check (nota is null or (nota >= 0 and nota <= 10)),
  favorito boolean not null default false,
  comentario text,
  data_inicio date,
  data_conclusao date,
  updated_at timestamptz default now(),
  unique (livro_id, perfil_id)
);

