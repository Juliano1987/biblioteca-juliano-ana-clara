do $$ begin create policy "leituras_write" on public.leituras for all using (true) with check (true); exception when duplicate_object then null; end $$;

create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

drop trigger if exists livros_set_updated_at on public.livros;
create trigger livros_set_updated_at before update on public.livros for each row execute function public.set_updated_at();

drop trigger if exists leituras_set_updated_at on public.leituras;
create trigger leituras_set_updated_at before update on public.leituras for each row execute function public.set_updated_at();
